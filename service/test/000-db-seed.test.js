const SPECIAL_NAMES = new Map([
  ["00000000-0000-0000-0000-000000000101", "Org Admin 1"],
  ["00000000-0000-0000-0000-000000000201", "Coach 1"],
  ["00000000-0000-0000-0000-000000000202", "Coach 2"],
  ["00000000-0000-0000-0000-000000000301", "Random Staff"],
  ["00000000-0000-0000-0000-000000000901", "Platform Admin"],
]);

function buildUserSeed(id) {
  const normalized = id.replace(/-/g, "");
  return {
    id,
    name: SPECIAL_NAMES.get(id) ?? `Seed User ${id.slice(-4).toUpperCase()}`,
    email: `seed+${normalized}@example.com`,
    externalUid: `ext-${normalized}`,
  };
}

const USER_SEEDS = USER_IDS.map(buildUserSeed);

test("db bootstrap: ensure canonical users exist", async (t) => {
  if (!DB_ENABLED) {
    t.diagnostic("DATABASE_URL not set; skipping db bootstrap");
    return;
  }
  const { query } = await import("../src/db/client.js");
  for (const seed of USER_SEEDS) {
    await query(
      `INSERT INTO users (id, external_uid, email, name)
       VALUES ($1,$2,$3,$4)
       ON CONFLICT (id) DO UPDATE SET
         external_uid = EXCLUDED.external_uid,
         email = EXCLUDED.email,
         name = EXCLUDED.name`,
      [seed.id, seed.externalUid, seed.email, seed.name]
    );
  }
  t.diagnostic(`Seeded ${USER_SEEDS.length} user rows`);
});
